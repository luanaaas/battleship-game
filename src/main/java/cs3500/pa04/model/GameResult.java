package cs3500.pa04.model;

/**
 * Represents the game result enum of either win, loss, or draw
 */
public enum GameResult {
  /**
   * Represent the win state of a game
   */
  WIN,
  /**
   * Represents the loss result of the player of the game
   */
  LOSE,
  /**
   * Represents how a player can draw with their opponent
   */
  DRAW;
}
